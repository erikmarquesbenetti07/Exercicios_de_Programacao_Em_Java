public class Calculadora {
    int operando1;
    int operando2;

    int somar(){
        return (operando1 + operando2);
    }

    int subtrair() {
        return (operando1 - operando2);
    }

    int multiplicar() {
        return (operando1 * operando2);
    }

    double dividir() {
        return (operando1 / operando2);
    }
}
